// Retourne une fonction de traduction tr(key) pour la langue donnée (fallback EN).
export const makeReportT = (currentLanguage) => {
  const code = getLanguageCode(currentLanguage);
  const dict = reportTranslations[code] || reportTranslations.en;
  return (key) => dict[key] ?? reportTranslations.en[key] ?? key;
};
